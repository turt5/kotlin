package Basics

fun main() {
    val num1=12.5;
    val num2=3.5;

    var add:Double=num1+num2;
    var sub:Double=num1-num2;
    var mult:Double=num1*num2;
    var div:Double=num1/num2;
    var mod=num1%num2;



    println(add);
    println(sub);
    println(mult);
    println(div);
    println(mod);
    println(++add);
    println(--add);


    println(add>sub);
    println(add<sub);
    println(add==sub);


}